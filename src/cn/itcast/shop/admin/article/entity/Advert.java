package cn.itcast.shop.admin.article.entity;

import java.util.Date;


/**
 * 广告
 * @author Administrator
 *
 */
public class Advert {

	Integer id;
	
	String name;
	
	String link;
	
	
	AdvertType advertType;
	
	String res;
	
	
	Adsense adsense;
	
	
	Date startDate;
	
	Date endDate;
	
	Integer sort;
	
	Boolean active;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public AdvertType getAdvertType() {
		return advertType;
	}

	public void setAdvertType(AdvertType advertType) {
		this.advertType = advertType;
	}

	public String getRes() {
		return res;
	}

	public void setRes(String res) {
		this.res = res;
	}

	public Adsense getAdsense() {
		return adsense;
	}

	public void setAdsense(Adsense adsense) {
		this.adsense = adsense;
	}

	public Date getStartDate() {
		return startDate;
	}

	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public Integer getSort() {
		return sort;
	}

	public void setSort(Integer sort) {
		this.sort = sort;
	}

	public Boolean getActive() {
		return active;
	}

	public void setActive(Boolean active) {
		this.active = active;
	}

	@Override
	public String toString() {
		return "Advert [id=" + id + ", name=" + name + ", link=" + link
				+ ", advertType=" + advertType + ", res=" + res + ", adsense="
				+ adsense + ", startDate=" + startDate + ", endDate=" + endDate
				+ ", sort=" + sort + ", active=" + active + "]";
	}
	
	
}
